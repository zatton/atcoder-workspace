#!/usr/bin/env python3
import sys

YES = "Yes"  # type: str
NO = "No"  # type: str


def solve(H: int, W: int, s: "List[str]"):
    for i in range(H):
        for j in range(W):
            if s[i][j] == '#':
                if i > 0 and s[i - 1][j] == '#':
                    continue
                elif j > 0 and s[i][j - 1] == '#':
                    continue
                elif i < H - 1 and s[i + 1][j] == '#':
                    continue
                elif j < W - 1 and s[i][j + 1] == '#':
                    continue
                print(NO)
                return
    print(YES)
    return


